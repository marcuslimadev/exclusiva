        Log::info('üì± De: ' . ($webhookData['From'] ?? 'N/A'));
        Log::info('üë§ Nome: ' . ($webhookData['ProfileName'] ?? 'N/A'));
        Log::info('üí¨ Mensagem: ' . ($webhookData['Body'] ?? '[m√≠dia]'));
        Log::info('üÜî MessageSid: ' . ($webhookData['MessageSid'] ?? 'N/A'));
        Log::info('üìç Cidade: ' . ($webhookData['FromCity'] ?? 'N/A'));
        Log::info('üó∫Ô∏è  Estado: ' . ($webhookData['FromState'] ?? 'N/A'));
        Log::info('üåç Pa√≠s: ' . ($webhookData['FromCountry'] ?? 'N/A'));
        if (isset($webhookData['Latitude']) && isset($webhookData['Longitude'])) {
            Log::info('üìå Coordenadas: ' . $webhookData['Latitude'] . ', ' . $webhookData['Longitude']);
        }
        Log::info('‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ');
        
        try {
            $result = $this->whatsappService->processIncomingMessage($webhookData);
            
            Log::info('‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó');
